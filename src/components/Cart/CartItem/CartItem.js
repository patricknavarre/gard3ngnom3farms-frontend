import React, { useState } from "react";
import {
  adjustItemQty,
  removeFromCart,
} from "../../../redux/Shopping/shopping-action-creators";
import { connect } from "react-redux";
import styles from "./CartItem.module.css";

const CartItem = ({ item, adjustQty, removeFromCart }) => {
  const [input, setInput] = useState(item.qty);

  const onChangeHandler = (e) => {
    setInput(e.target.value);
    adjustQty(item.id, e.target.value);
  };

  return (
    <div className={styles.cartItem}>
      <img
        className={styles.cartItem_image}
        src={item.image}
        alt={item.title}
      />
      <div className={styles.cartItem_details}>
        <p className={styles.details_title}>{item.title}</p>
        <p className={styles.details_desc}>{item.description}</p>
        <p className={styles.details_price}>$ {item.price}</p>
      </div>
      <div className={styles.cartItem_actions}>
        <div className={styles.cartItem_qty}>
          <label htmlFor="qty">Qty</label>
          <input
            min="1"
            type="number"
            id="qty"
            name="qty"
            value={input}
            onChange={onChangeHandler}
          />
        </div>
        <button
          onClick={() => removeFromCart(item.id)}
          className={styles.actions_deleteItemBtn}
        >
          <img 
            src="https://image.flaticon.com/icons/svg/709/709519.svg"
            alt=""
          />
        </button>
      </div>
    </div>
  );
};

const mapDispatchToProps = (dispatch) => {
  return {
    adjustQty: (id, value) => dispatch(adjustItemQty(id, value)),
    removeFromCart: (id) => dispatch(removeFromCart(id)),
  };
};

export default connect(null, mapDispatchToProps)(CartItem);
